/*
 *       unsigned long long _SoloLoadDouble(unsigned long long *addr) ; 
 *
 *       Perform a load double to the specified address. Assumes a
 *       "broken" ld instruction.
 *
 */
	.globl  _LoadDouble					
	.ent    _LoadDouble,0                                    
_LoadDouble:                                                     
	.frame  sp,0,ra
        /* This assumes ld does a laod into v0 and v1 */
         ldc2   v0,(a0)
	j ra
	.end _LoadDouble

